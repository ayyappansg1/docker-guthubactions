package common.Pojo;

public class ContactDetails {
    private OfficeLandline officeLandline;
    private HomeMobile homeMobile;
    private HomeLandline homeLandline;
    private String homeEmail;

    public OfficeLandline getOfficeLandline() {
        return officeLandline;
    }

    public void setOfficeLandline(OfficeLandline officeLandline) {
        this.officeLandline = officeLandline;
    }

    public HomeMobile getHomeMobile() {
        return homeMobile;
    }

    public void setHomeMobile(HomeMobile homeMobile) {
        this.homeMobile = homeMobile;
    }

    public HomeLandline getHomeLandline() {
        return homeLandline;
    }

    public void setHomeLandline(HomeLandline homeLandline) {
        this.homeLandline = homeLandline;
    }

    public String getHomeEmail() {
        return homeEmail;
    }

    public void setHomeEmail(String homeEmail) {
        this.homeEmail = homeEmail;
    }
}
